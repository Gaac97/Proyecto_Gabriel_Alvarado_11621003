    public static void PrintMatriz(String x[][], int f, int c) {

        if (f == x.length - 1 && c == x.length - 1) {
            System.out.print(x[f][c] + "");
        } else {
            if (c == x[0].length - 1) {
                System.out.println("["+x[f][c]+"]"+"\t");
                PrintMatriz(x, f + 1, 0);
            } else {
                System.out.print("["+x[f][c]+"]"+"\t");
                
                PrintMatriz(x, f, c + 1);
            }
            
        }
       
    }
    
    public static String[][] Eliminar(String y[][])
    {
        for (int i = 0; i < y.length; i++) {
            for (int j = 0; j < y[0].length; j++) {
               if(y[i][j]=="-"||y[i][j]=="|")
                   y[i][j] = " ";
                
            }
            
        }
        for (int i = 0; i < y.length; i++) {
            for (int j = 0; j < y.length; j++) {
                if (y[i][j] == "○") {
                    y[i][j]= "☺";
                    
                }
                
            }
            
        }
        return y;
       
    }
